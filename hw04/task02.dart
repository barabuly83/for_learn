/// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 10 (for)
void printNumbers() {
  print('–í—ã–≤–æ–¥ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 10:');
  
  // –ü—Ä–æ—Å—Ç–æ–π —Ü–∏–∫–ª for
  for (int i = 1; i <= 10; i++) {
    print('  –ß–∏—Å–ª–æ: $i');
  }
  
  // –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è for
  print('\n –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –ø—Ä–∏–º–µ—Ä—ã —Å for:');
  
  // 1. –ß–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞
  print('–ß–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 10:');
  for (int i = 1; i <= 10; i++) {
    if (i % 2 == 0) {
      print('  $i');
    }
  }
  
  // 2. –û–±—Ä–∞—Ç–Ω—ã–π –æ—Ç—Å—á–µ—Ç
  print('–û–±—Ä–∞—Ç–Ω—ã–π –æ—Ç—Å—á–µ—Ç –æ—Ç 10 –¥–æ 1:');
  for (int i = 10; i >= 1; i--) {
    print('  $i');
  }
  
  // 3. –°—É–º–º–∞ —á–∏—Å–µ–ª
  print('–°—É–º–º–∞ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 10:');
  int sum = 0;
  for (int i = 1; i <= 10; i++) {
    sum += i;
    if (i < 10) {
      stdout.write('$i + ');
    } else {
      stdout.write('$i = ');
    }
  }
  print('$sum');
}

